    @property
    def categories(self) -> dict:
        """Return {category_name: {host: Sites}}."""
        return {
            "social": dict(self.SOCIAL),
            "forums": dict(self.FORUMS),
            "blogs": dict(self.BLOGS),
            "gaming": dict(self.GAMING),
            "dev": dict(self.DEV),
            "creative": dict(self.CREATIVE),
            "misc": dict(self.MISC),
            "professional": dict(self.PROFESSIONAL),
            "people_search": dict(self.PEOPLE_SEARCH),
            "links": dict(self.LINKS),
        }

    @property
    def all(self) -> list:
        """Flat, de-duplicated, sorted list of every site (key)."""
        seen = set()
        for sites in self.categories.values():
            seen.update(sites.keys())
        return sorted(s for s in seen if "." in s)

    @property
    def https_urls(self) -> list:
        """Sorted list of every site as a full https:// URL."""
        return [f"https://{site}" for site in self.all]

    @property
    def urls_return_code(self) -> dict:
        """Sites checked by HTTP status code (int or list[int])."""
        return {s: o for s, o in self._iter() if isinstance(o.type, (int, list))}

    @property
    def urls_return_text(self) -> dict:
        """Sites checked by HTML substring (str or list[str])."""
        return {
            s: o for s, o in self._iter()
            if isinstance(o.type, (str, list)) and o.type
            and isinstance(o.type[0], str)
        }

    def _iter(self):
        for sites in self.categories.values():
            yield from sites.items()

    def get_type_url(self, site: str) -> str:
        """code / html / redirect / dynamic."""
        for sites in self.categories.values():
            if site in sites:
                o = sites[site]
                t = o.type
                if isinstance(t, (int, list)) and t and not isinstance(t[0], str):
                    return "code"
                if isinstance(t, (str, list)) and t and isinstance(t[0], str):
                    return "html"
                if o.info and ("error" in o.info or "probe" in o.info):
                    return "redirect"
                return "dynamic"
        return "dynamic"
